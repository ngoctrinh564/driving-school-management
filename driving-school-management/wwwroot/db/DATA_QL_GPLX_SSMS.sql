   Ghi chú quan trọng:
   - Script cũ dùng ROW_NUMBER() chỉ trên tập INSERTED.
   - Như vậy nếu insert nhiều đợt khác nhau, tên khóa học có thể bị lặp K001, K002...
   - Bản dưới đây tính theo toàn bộ dữ liệu hiện có của từng hạng để tránh trùng tốt hơn.
========================================================= */
CREATE OR ALTER TRIGGER trg_AutoTenKhoaHoc
ON KhoaHoc
AFTER INSERT
AS
BEGIN
    SET NOCOUNT ON;

    ;WITH cte AS (
        SELECT
            kh.khoaHocId,
            kh.hangId,
            ROW_NUMBER() OVER (
                PARTITION BY kh.hangId
                ORDER BY kh.khoaHocId
            ) AS rn
        FROM KhoaHoc kh
    )
    UPDATE kh
    SET tenKhoaHoc =
        N'Khóa học lái xe hạng ' + hg.tenHang +
        N' - K' + RIGHT('00' + CAST(cte.rn AS VARCHAR(3)), 3)
    FROM KhoaHoc kh
    INNER JOIN cte
        ON kh.khoaHocId = cte.khoaHocId
    INNER JOIN HangGplx hg
        ON kh.hangId = hg.hangId
    INNER JOIN inserted i
        ON kh.khoaHocId = i.khoaHocId;
END;
GO


/* =========================================================
   19. CAP NHAT TEN KHOA HOC CHO DU LIEU DA CO
========================================================= */
;WITH cte AS (
    SELECT
        khoaHocId,
        hangId,
        ROW_NUMBER() OVER (
            PARTITION BY hangId
            ORDER BY khoaHocId
